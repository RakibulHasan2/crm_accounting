function SidebarContent({ 
  navigation, 
  pathname 
}: { 
  navigation: Array<{ name: string; href: string; icon: React.ReactNode }>; 
  pathname: string;
}) {
  return (
    <>
      <div className="flex items-center h-16 flex-shrink-0 px-4 bg-gray-900">
        <h1 className="text-white text-lg font-semibold">Admin Panel</h1>
      </div>
      <div className="flex-1 flex flex-col overflow-y-auto">
        <nav className="flex-1 px-2 py-4 bg-gray-800 space-y-1">
          {navigation.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.name}
                href={item.href}
                className={`group flex items-center px-2 py-2 text-sm font-medium rounded-md ${
                  isActive
                    ? 'bg-gray-900 text-white'
                    : 'text-gray-300 hover:bg-gray-700 hover:text-white'
                }`}
              >
                <span className={`mr-3 h-6 w-6 ${isActive ? 'text-gray-300' : 'text-gray-400 group-hover:text-gray-300'}`}>
                  {item.icon}
                </span>
                {item.name}
              </Link>
            );
          })}
        </nav>
      </div>
    </>
  );
}